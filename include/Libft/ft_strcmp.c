int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	if (!s1 || !s2)
		return (1);
	while (s1[i] && s2[i])
	{
		if (s1[i] != s2[i])
			return (s2[i] - s1[i]);
		i++;
	}
	if (s1[i] == '\0' && s1[i] == s2[i])
		return (0);
	return (s2[i] - s1[i]);
}
