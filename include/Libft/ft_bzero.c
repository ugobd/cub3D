#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*val;

	i = 0;
	val = (unsigned char *)s;
	while (i < n)
	{
		val[i] = '\0';
		i++;
	}
}
